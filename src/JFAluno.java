        alu.setBairro(jTFbairro.getText());
        alu.setCep(jTFcep.getText());
        alu.setCidade(jTFcidade.getText());
        alu.setUf(jTFuf.getText());
        alu.setObs(jTFobservacao.getText());
        alu.setDataCadastro(jTFdataCad.getText());
