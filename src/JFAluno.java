        sRa = (String) jTable1.getModel().getValueAt(  jTable1.getSelectedRow(),0  );  // recupera o RA da linha selecionada - coluna zero
        AlunoDAO dao;
        dao = new AlunoDAO();
        
        Aluno a;
        a = new Aluno();
        a = dao.consultaAlunoPorRA(sRa);
        
        carregarFormulario(a);
        
        ((DefaultTableModel) jTable1.getModel()).setNumRows(0);   // redefine a tabela com "zero linhas"
